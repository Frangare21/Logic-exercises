// Operadores en javascript:

// Aritmeticos:
// Operador de suma (+):
let num1 = 10;
let num2 = 12;
let result1 = num1 + num2;
console.log("forma 1 de sumar: ", result1);
console.log("forma 2 de sumar: ", num1 + num2);
console.log("forma 3 de sumar: ", num1 + 15);

// Operador de sustraccion (-):
num1 = 15;
num2 = 10;
let result2 = num1 - num2;
console.log("resta: ", result2);

// Operador de multiplicacion (*):
num1 = 4;
num2 = 10;
let result3 = num1 * num2;
console.log("multiplicacion: ", result3);

// Operador de division (/):
num1 = 40;
num2 = 10;
let result4 = num1 / num2;
console.log("division: ", result4);


// Operador de modulo (%):
/*
   Este operador puede ser dificil de entender.
   En la informatica el modulo es el operador utilizado
   para obtener el resto de una division entre 2 numeros.
*/
num1 = 50;
num2 = 3;
let result5 = num1 % num2;
console.log("modulo o resto de la division: ", result5);

// Operador de exponenciacion (**):
num1 = 3;
console.log("exponenciancion: ", num1 ** 3);

// Operador de incremento (++):
num1 = 50;
console.log("incremento: ", num1++);

// Operador de decremento (--):
num1 = 30;
console.log("decremento: ", num1--);

//--------------------------------

// Operadores de asignacion:

// El operador de asignacion es el que venimos utilizando
// Operador (=):
let variable = undefined;
console.log("variable indefinida: ", variable);

// Operador de asignacion de suma (+=):
num1 = 15;
num2 = 40;
num1 += num2;
console.log("asignacion de suma: ", num1);

// Operador de asignacion de resta (-=):
num1 = 50;
num2 = 10;
num1 -= num2;
console.log("asignacion de resta: ", num1);

// Operador de asignacion de multiplicacion (*=):
num1 = 5;
num2 = 10;
num1 *= num2;
console.log("asignacion de multiplicacion: ", num1);

// Operador de asignacion de division (/=):
num1 = 30;
num2 = 3;
num1 /= num2;
console.log("asignacion de division: ", num1);
